                    require the delivery to be properly protected by a security arrangement it specifies.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A proxy
                    notice may specify how the proxy appointed under it is to vote (or that the proxy is to abstain
                    from voting) on one or more resolutions dealing with any business to be transacted at a general
                    meeting.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Unless a
                    proxy notice indicates otherwise, it must be regarded as—</p>
                <ol id="l131">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            allowing the person appointed under it as a proxy discretion as to how to vote on any
                            ancillary or procedural resolutions put to the general meeting; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            appointing that person as a proxy in relation to any adjournment of the general meeting
                            to which it relates as well as the meeting itself.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
