        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Unanimous
            decisions</p>
        <ol id="l39">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A decision of
                    the directors is taken in accordance with this article when all eligible directors indicate to
                    each other (either directly or indirectly) by any means that they share a common view on a
