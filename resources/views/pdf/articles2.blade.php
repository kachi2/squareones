            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">if the deceased
                    member was a sole holder of the share, the legal personal representative of the deceased member.
                </p>
            </li>
        </ol>
    </li>
