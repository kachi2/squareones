                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            instrument of transfer is not accompanied by the certificate for the share to which it
                            relates, or other evidence the directors reasonably require to show the transferor’s
                            right to make the transfer, or evidence of the right of someone other than the
                            transferor to make the transfer on the transferor’s behalf; or</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -24pt;text-align: justify;">the
                            transfer is in respect of more than one class of shares.</p>
