        <ol id="l32">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Subject to these
                    articles, the directors may, if they think fit, delegate any of the powers that are conferred on
                    them under these articles—</p>
