            </li>
        </ol>
    </li>
    <li data-list-text="64.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Power of
            directors to refuse transfer of shares</p>
        <ol id="l156">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Without
                    limiting article 2(2), the directors may refuse to register the transfer of a share if—</p>
                <ol id="l157">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
