                    eligible director or to which each eligible director has otherwise indicated agreement in
                    writing.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A reference
                    in this article to eligible directors is a reference to directors who would have been entitled
                    to vote on the matter if it had been proposed as a resolution at a directors’ meeting.</p>
