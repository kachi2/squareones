                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">in
                            relation to any matter; and</p>
                    </li>
                    <li data-list-text="(e)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">on any
                            terms and conditions.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If the directors
                    so specify, the delegation may authorize further delegation of the directors’ powers by any
                    person to whom they are delegated.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may—</p>
                <ol id="l34">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">revoke
                            the delegation wholly or in part; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">revoke
                            or alter its terms and conditions.</p>
                    </li>
                </ol>
            </li>
        </ol>
