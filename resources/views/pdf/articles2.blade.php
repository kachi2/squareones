                    guardian appointed by the Court.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The
                    committee, receiver, guardian or other person may vote by proxy on a show of hands or on a poll.
                </p>
            </li>
        </ol>
    </li>
    <li data-list-text="49.">
        <p class="s7" style="padding-top: 3pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Content of
