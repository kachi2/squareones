                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">its
                            proposed date and time; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">where it
                            is to take place.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Notice of a
                    directors’ meeting must be given to each director, but need not be in writing.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="10.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Participation in directors’ meetings</p>
        <ol id="l42">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    these articles, directors participate in a directors’ meeting, or part of a directors’ meeting,
                    when—</p>
                <ol id="l43">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the
                            meeting has been called and takes place in accordance with these articles; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">they can
                            each communicate to the others any information or opinions they have on any particular
                            item of the business of the meeting.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">In determining
                    whether directors are participating in a directors’ meeting, it is irrelevant where a director
                    is and how they communicate with each other.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If all the
                    directors participating in a directors’ meeting are not in the same place, they may regard the
                    meeting as taking place wherever any one of them is.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="11.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Quorum for
            directors’ meetings</p>
        <ol id="l44">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">At a directors’
                    meeting, unless a quorum is participating, no proposal is to be voted on, except a proposal to
                    call another meeting.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The quorum for
                    directors’ meetings may be fixed from time to time by a decision of the directors and unless
                    otherwise fixed it is 2.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="12.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Meetings if
            total number of directors less than quorum</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">If the total number of
            directors for the time being is less than the quorum required for directors’ meetings, the directors
            must not take any decision other than a decision—</p>
        <ol id="l45">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">to appoint
                    further directors; or</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">to call a
                    general meeting so as to enable the members to appoint further directors.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="13.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Chairing of
            directors’ meetings</p>
        <ol id="l46">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may appoint a director to chair their meetings.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The person
                    appointed for the time being is known as the chairperson.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may terminate the appointment of the chairperson at any time.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    chairperson is not participating in a directors’ meeting within 10 minutes of the time at which
                    it was to start or is unwilling to chair the meeting, the participating directors may appoint
                    one of themselves to chair it.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="14.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Chairperson’s casting vote at directors’ meetings</p>
        <ol id="l47">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    numbers of votes for and against a proposal are equal, the chairperson or other director
                    chairing the directors’ meeting has a casting vote.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph (1)
                    does not apply if, in accordance with these articles, the chairperson or other director is not
                    to be counted as participating in the decision-making process for quorum or voting purposes.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="15.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Alternates
            voting at directors’ meetings</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">A director who is also an
            alternate director has an additional vote on behalf of each appointor who—</p>
        <ol id="l48">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">is not
                    participating in a directors’ meeting; and</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">would have been
                    entitled to vote if he or she were participating in it.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="16.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Conflicts
            of interest</p>
        <ol id="l49">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">This article
                    applies if—</p>
                <ol id="l50">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">a
                            director is in any way (directly or indirectly) interested in a transaction, arrangement
                            or contract with the company that is significant in relation to the company’s business;
                            and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            director’s interest is material.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The director
                    must declare the nature and extent of the director’s interest to the other directors in
                    accordance with section 536 of the Ordinance.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The director
                    and the director’s alternate must neither—</p>
                <ol id="l51">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">vote
                            in respect of the transaction, arrangement or contract in which the director is so
                            interested; nor</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">be
                            counted for quorum purposes in respect of the transaction, arrangement or contract.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph (3)
                    does not preclude the alternate from—</p>
                <ol id="l52">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            voting in respect of the transaction, arrangement or contract on behalf of another
                            appointor who does not have such an interest; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">being
                            counted for quorum purposes in respect of the transaction, arrangement or contract.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    director or the director’s alternate contravenes paragraph (3)(a), the vote must not be counted.
                </p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph (3)
                    does not apply to—</p>
                <ol id="l53">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">an
                            arrangement for giving a director any security or indemnity in respect of money lent by
                            the director to or obligations undertaken by the director for the benefit of the
                            company;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">an
                            arrangement for the company to give any security to a third party in respect of a debt
                            or obligation of the company for which the director has assumed responsibility wholly or
                            in part under a guarantee or indemnity or by the deposit of a security;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -24pt;text-align: justify;">an
                            arrangement under which benefits are made available to employees and directors or former
                            employees and directors of the company or any of its subsidiaries, which do not provide
                            special benefits for directors or former directors; or</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">an
                            arrangement to subscribe for or underwrite shares.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(7)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A reference in
                    this article (except in paragraphs (6)(d) and (8)) to a transaction, arrangement or contract
                    includes a proposed transaction, arrangement or contract.</p>
            </li>
            <li data-list-text="(8)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">In this article—
                </p>
                <p style="padding-top: 3pt;padding-left: 47pt;text-indent: 0pt;text-align: left;"><i><b>arrangement
                            to subscribe for or underwrite shares </b></i>(<span class="s6">認購或包銷股份安排</span>) means—
                </p>
                <ol id="l54">
                    <li data-list-text="(a)">
                        <p style="padding-top: 2pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">a
                            subscription or proposed subscription for shares or other securities of the company;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">an
                            agreement or proposed agreement to subscribe for shares or other securities of the
                            company; or</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">an
                            agreement or proposed agreement to underwrite any of those shares or securities.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="17.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Supplementary provisions as to conflicts of interest</p>
        <ol id="l55">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director
                    may hold any other office or position of profit under the company (other than the office of
                    auditor and if the company has only 1 director, the office of company secretary) in conjunction
                    with the office of director for a period and on terms (as to remuneration or otherwise) that the
                    directors determine.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director or
                    intending director is not disqualified by the office of director from contracting with the
                    company—</p>
                <ol id="l56">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">with
                            regard to the tenure of the other office or position of profit mentioned in paragraph
                            (1); or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">as
                            vendor, purchaser or otherwise.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The contract
                    mentioned in paragraph (2) or any transaction, arrangement or contract entered into by or on
                    behalf of the company in which any director is in any way interested is not liable to be
                    avoided.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director
                    who has entered into a contract mentioned in paragraph (2) or is interested in a transaction,
                    arrangement or contract mentioned in paragraph (3) is not liable to account to the company for
                    any profit realized by the transaction, arrangement or contract by reason of—</p>
                <ol id="l57">
                    <li data-list-text="(a)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            director holding the office; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            fiduciary relation established by the office.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph
                    (1), (2), (3) or (4) only applies if the director has declared the nature and extent of the
                    director’s interest under the paragraph to the other directors in accordance with section 536 of
                    the Ordinance.</p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director of
                    the company may be a director or other officer of, or be otherwise interested in—</p>
                <ol id="l58">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            company promoted by the company; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            company in which the company may be interested as shareholder or otherwise.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(7)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    the Ordinance, the director is not accountable to the company for any remuneration or other
                    benefits received by the director as a director or officer of, or from the director’s interest
                    in, the other company unless the company otherwise directs.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="18.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Validity
            of acts of meeting of directors</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">The acts of any meeting
            of directors or of a committee of directors or the acts of any person acting as a director are as valid
            as if the directors or the person had been duly appointed as a director and was qualified to be a
            director, even if it is afterwards discovered that—</p>
        <ol id="l59">
            <li data-list-text="(a)">
                <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">there was a
                    defect in the appointment of any of the directors or of the person acting as a director;</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">any one or more
                    of them were not qualified to be a director or were disqualified from being a director;</p>
            </li>
            <li data-list-text="(c)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">any one or more
                    of them had ceased to hold office as a director; or</p>
            </li>
            <li data-list-text="(d)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">any one or more
                    of them were not entitled to vote on the matter in question.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="19.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Record of
            decisions to be kept</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">The directors must
            ensure that the company keeps a written record of every decision taken by the directors under article
            7(1) for at least 10 years from the date of the decision.</p>
    </li>
    <li data-list-text="20.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Written
            record of decision of sole director</p>
        <ol id="l60">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">This article
                    applies if the company has only 1 director and the director takes any decision that</p>
                <ol id="l61">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">may be
                            taken in a directors’ meeting; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">has
                            effect as if agreed in a directors’ meeting.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The director
                    must provide the company with a written record of the decision within 7 days after the decision
                    is made.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The director is
                    not required to comply with paragraph (2) if the decision is taken by way of a resolution in
                    writing.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If the decision
                    is taken by way of a resolution in writing, the company must keep the resolution for at least 10
                    years from the date of the decision.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The company must
                    also keep a written record provided to it in accordance with paragraph</p>
                <p style="padding-left: 71pt;text-indent: 0pt;text-align: left;">(2) for at least 10 years from the
                    date of the decision.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="21.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Directors’
            discretion to make further rules</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">Subject to these articles,
            the directors may make any rule that they think fit about—</p>
        <ol id="l62">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">how they take
                    decisions; and</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">how the rules
                    are to be recorded or communicated to directors.</p>
            </li>
        </ol>
        <h4 style="padding-top: 12pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division 3—Appointment
            and Retirement of Directors</h4>
    </li>
    <li data-list-text="22.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Appointment
            and retirement of directors</p>
        <ol id="l63">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A person who is
                    willing to act as a director, and is permitted by law to do so, may be appointed to be a
                    director—</p>
                <ol id="l64">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">by
                            ordinary resolution; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">by a
                            decision of the directors.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Unless
                    otherwise specified in the appointment, a director appointed under paragraph (1)(a) holds office
                    for an unlimited period of time.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">An
                    appointment under paragraph (1)(b) may only be made to—</p>
                <ol id="l65">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">fill
                            a casual vacancy; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            appoint a director as an addition to the existing directors if the total number of
                            directors does not exceed the number fixed in accordance with these articles.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director
                    appointed under paragraph (1)(b) must—</p>
                <ol id="l66">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            retire from office at the next annual general meeting following the appointment; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if
                            the company has dispensed with the holding of annual general meetings or is not required
                            to hold annual general meetings, retire from office before the end of 9 months after the
                            end of the company’s accounting reference period by reference to which the financial
                            year in which the director was appointed is to be determined.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="23.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Retiring
            director eligible for reappointment</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">A retiring director is
            eligible for reappointment to the office.</p>
    </li>
    <li data-list-text="24.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Composite
            resolution</p>
        <ol id="l67">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">This article
                    applies if proposals are under consideration concerning the appointment of 2 or more directors
                    to offices or employments with the company or any other body corporate.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The proposals
                    may be divided and considered in relation to each director separately.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Each of the
                    directors concerned is entitled to vote (if the director is not for another reason precluded
                    from voting) and be counted in the quorum in respect of each resolution except that concerning
                    the director’s own appointment.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="25.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Termination of director’s appointment</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">A person ceases to be a
            director if the person—</p>
        <ol id="l68">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: justify;">ceases to be
                    a director under the Ordinance or the Companies (Winding Up and Miscellaneous Provisions)
                    Ordinance (Cap. 32) or is prohibited from being a director by law;</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: justify;">becomes
                    bankrupt or makes any arrangement or composition with the person’s creditors generally;</p>
            </li>
            <li data-list-text="(c)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: justify;">becomes a
                    mentally incapacitated person;</p>
            </li>
            <li data-list-text="(d)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: left;">resigns the
                    office of director by notice in writing of the resignation in accordance with section 464(5) of
                    the Ordinance;</p>
            </li>
            <li data-list-text="(e)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: left;">for more than 6
                    months has been absent without the directors’ permission from directors’ meetings held during
                    that period; or</p>
            </li>
            <li data-list-text="(f)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: left;">is removed from
                    the office of director by an ordinary resolution of the company.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="26.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Directors’
            remuneration</p>
        <ol id="l69">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Directors’
                    remuneration must be determined by the company at a general meeting.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A director’s
                    remuneration may—</p>
                <ol id="l70">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">take any
                            form; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">include
                            any arrangements in connection with the payment of a retirement benefit to or in respect
                            of that director.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Directors’
                    remuneration accrues from day to day.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="27.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Directors’
            expenses</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">The company may pay any
            travelling, accommodation and other expenses properly incurred by directors in connection with—</p>
        <ol id="l71">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">their attendance
                    at—</p>
                <ol id="l72">
                    <li data-list-text="(i)">
                        <p style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: left;">
                            meetings of directors or committees of directors;</p>
                    </li>
                    <li data-list-text="(ii)">
                        <p style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: left;">general
                            meetings; or</p>
                    </li>
                    <li data-list-text="(iii)">
                        <p style="padding-top: 4pt;padding-left: 119pt;text-indent: -26pt;text-align: left;">
                            separate meetings of the holders of any class of shares or of debentures of the company;
                            or</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the exercise of
                    their powers and the discharge of their responsibilities in relation to the company.</p>
            </li>
        </ol>
        <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division 4—Alternate
            Directors</h4>
    </li>
    <li data-list-text="28.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Appointment and removal of alternates</p>
        <ol id="l73">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director
                    (<i><b>appointor</b></i>) may appoint as an alternate any other director, or any other person
                    approved by resolution of the directors.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">An alternate
                    may exercise the powers and carry out the responsibilities of the alternate’s appointor, in
                    relation to the taking of decisions by the directors in the absence of the alternate’s
                    appointor.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">An
                    appointment or removal of an alternate by the alternate’s appointor must be effected—</p>
                <ol id="l74">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">by
                            notice to the company; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">in
                            any other manner approved by the directors.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The notice
                    must be authenticated by the appointor.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The notice
                    must—</p>
                <ol id="l75">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            identify the proposed alternate; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if it
                            is a notice of appointment, contain a statement authenticated by the proposed alternate
                            indicating the proposed alternate’s willingness to act as the alternate of the
                            appointor.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If an
                    alternate is removed by resolution of the directors, the company must as soon as practicable
                    give notice of the removal to the alternate’s appointor.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="29.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Rights and
            responsibilities of alternate directors</p>
        <ol id="l76">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">An alternate
                    director has the same rights as the alternate’s appointor in relation to any decision taken by
                    the directors under article 7(1).</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Unless these
                    articles specify otherwise, alternate directors—</p>
                <ol id="l77">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">are
                            deemed for all purposes to be directors;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">are
                            liable for their own acts and omissions;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">are
                            subject to the same restrictions as their appointors; and</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">are
                            deemed to be agents of or for their appointors.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Subject to
                    article 16(3), a person who is an alternate director but not a director—</p>
                <ol id="l78">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">may be
                            counted as participating for determining whether a quorum is participating (but only if
                            that person’s appointor is not participating); and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">may sign
                            a written resolution (but only if it is not signed or to be signed by that person’s
                            appointor).</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">An alternate
                    director must not be counted or regarded as more than one director for determining whether—</p>
                <ol id="l79">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 96pt;text-indent: -24pt;text-align: left;">a quorum
                            is participating; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 96pt;text-indent: -24pt;text-align: left;">a
                            directors’ written resolution is adopted.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">An alternate
                    director is not entitled to receive any remuneration from the company for serving as an
                    alternate director.</p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">But the
                    alternate’s appointor may, by notice in writing made to the company, direct that any part of the
                    appointor’s remuneration be paid to the alternate.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="30.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Termination
            of alternate directorship</p>
        <ol id="l80">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">An alternate
                    director’s appointment as an alternate terminates—</p>
                <ol id="l81">
                    <li data-list-text="(a)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if
                            the alternate’s appointor revokes the appointment by notice to the company in writing
                            specifying when it is to terminate;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">on
                            the occurrence in relation to the alternate of any event which, if it occurred in
                            relation to the alternate’s appointor, would result in the termination of the
                            appointor’s appointment as a director;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: justify;">on
                            the death of the alternate’s appointor; or</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">when
                            the alternate’s appointor’s appointment as a director terminates.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    alternate was not a director when appointed as an alternate, the alternate’s appointment as an
                    alternate terminates if—</p>
                <ol id="l82">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            approval under article 28(1) is withdrawn or revoked; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            company by an ordinary resolution passed at a general meeting terminates the
                            appointment.</p>
                        <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">
                            Division 5—Directors’ Indemnity and Insurance</h4>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="31.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Indemnity
        </p>
        <ol id="l83">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director or
                    former director of the company may be indemnified out of the company’s assets against any
                    liability incurred by the director to a person other than the company or an associated company
                    of the company in connection with any negligence, default, breach of duty or breach of trust in
                    relation to the company or associated company (as the case may be).</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph (1)
                    only applies if the indemnity does not cover—</p>
                <ol id="l84">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            liability of the director to pay—</p>
                        <ol id="l85">
                            <li data-list-text="(i)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: justify;">
                                    a fine imposed in criminal proceedings; or</p>
                            </li>
                            <li data-list-text="(ii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                    a sum payable by way of a penalty in respect of non-compliance with any
                                    requirement of a regulatory nature; or</p>
                            </li>
                        </ol>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            liability incurred by the director—</p>
                        <ol id="l86">
                            <li data-list-text="(i)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: justify;">
                                    in defending criminal proceedings in which the director is convicted;</p>
                            </li>
                            <li data-list-text="(ii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                    in defending civil proceedings brought by the company, or an associated company
                                    of the company, in which judgment is given against the director;</p>
                            </li>
                            <li data-list-text="(iii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -26pt;text-align: justify;">
                                    in defending civil proceedings brought on behalf of the company by a member of
                                    the company or of an associated company of the company, in which judgment is
                                    given against the director;</p>
                            </li>
                            <li data-list-text="(iv)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -27pt;text-align: justify;">
                                    in defending civil proceedings brought on behalf of an associated company of the
                                    company by a member of the associated company or by a member of an associated
                                    company of the associated company, in which judgment is given against the
                                    director; or</p>
                            </li>
                            <li data-list-text="(v)">
                                <p
                                    style="padding-top: 3pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                    in connection with an application for relief under section 903 or 904 of the
                                    Ordinance in which the Court refuses to grant the director relief.</p>
                            </li>
                        </ol>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A reference
                    in paragraph (2)(b) to a conviction, judgment or refusal of relief is a reference to the final
                    decision in the proceedings.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">For the
                    purposes of paragraph (3), a conviction, judgment or refusal of relief—</p>
                <ol id="l87">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if
                            not appealed against, becomes final at the end of the period for bringing an appeal; or
                        </p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if
                            appealed against, becomes final when the appeal, or any further appeal, is disposed of.
                        </p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">For the
                    purposes of paragraph (4)(b), an appeal is disposed of if—</p>
                <ol id="l88">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">it is
                            determined, and the period for bringing any further appeal has ended; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">it is
                            abandoned or otherwise ceases to have effect.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="32.">
        <p class="s7" style="padding-top: 3pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Insurance
        </p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">The directors may
            decide to purchase and maintain insurance, at the expense of the company, for a director of the company,
            or a director of an associated company of the company, against—</p>
        <ol id="l89">
            <li data-list-text="(a)">
                <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any liability
                    to any person attaching to the director in connection with any negligence, default, breach of
                    duty or breach of trust (except for fraud) in relation to the company or associated company (as
                    the case may be); or</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any liability
                    incurred by the director in defending any proceedings (whether civil or criminal) taken against
                    the director for any negligence, default, breach of duty or breach of trust (including fraud) in
                    relation to the company or associated company (as the case may be).</p>
            </li>
        </ol>
        <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division 6—Company
            Secretary</h4>
    </li>
    <li data-list-text="33.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Appointment
            and removal of company secretary</p>
        <ol id="l90">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may appoint a company secretary for a term, at a remuneration and on conditions they think fit.
                </p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may remove a company secretary appointed by them.</p>
                <h3 style="padding-top: 12pt;padding-left: 5pt;text-indent: 0pt;text-align: center;">Part 4</h3>
                <h3 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Decision-taking
                    by Members</h3>
                <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division
                    1—Organization of General Meetings</h4>
            </li>
        </ol>
    </li>
    <li data-list-text="34.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">General
            meetings</p>
        <ol id="l91">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    sections 611, 612 and 613 of the Ordinance, the company must, in respect of each financial year
                    of the company, hold a general meeting as its annual general meeting in accordance with section
                    610 of the Ordinance.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may, if they think fit, call a general meeting.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    directors are required to call a general meeting under section 566 of the Ordinance, they must
                    call it in accordance with section 567 of the Ordinance.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    directors do not call a general meeting in accordance with section 567 of the Ordinance, the
                    members who requested the meeting, or any of them representing more than one half of the total
                    voting rights of all of them, may themselves call a general meeting in accordance with section
                    568 of the Ordinance.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="35.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Notice of
            general meetings</p>
        <ol id="l92">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">An annual
                    general meeting must be called by notice of at least 21 days in writing.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A general
                    meeting other than an annual general meeting must be called by notice of at least 14 days in
                    writing.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The notice is
                    exclusive of—</p>
                <ol id="l93">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the day
                            on which it is served or deemed to be served; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the day
                            for which it is given.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The notice must—
                </p>
                <ol id="l94">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">specify
                            the date and time of the meeting;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">specify
                            either or both of the following—</p>
                        <ol id="l95">
                            <li data-list-text="(i)">
                                <p
                                    style="padding-top: 4pt;padding-left: 114pt;text-indent: -20pt;text-align: left;">
                                    the physical venue of the meeting;</p>
                            </li>
                            <li data-list-text="(ii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 115pt;text-indent: -23pt;text-align: left;">
                                    the virtual meeting technology to be used for holding the meeting;</p>
                                <p style="padding-top: 4pt;padding-left: 96pt;text-indent: -28pt;text-align: left;">
                                    (ba) if 2 or more physical venues are specified under subparagraph
                                    (b)(i)—specify the principal venue, and the other venue or venues, of the
                                    meeting;</p>
                            </li>
                        </ol>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-left: 95pt;text-indent: -24pt;text-align: left;">state the general nature
                            of the business to be dealt with at the meeting;</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">for a
                            notice calling an annual general meeting, state that the meeting is an annual general
                            meeting;</p>
                    </li>
                    <li data-list-text="(e)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">if a
                            resolution (whether or not a special resolution) is intended to be moved at the meeting—
                        </p>
                        <ol id="l96">
                            <li data-list-text="(i)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: justify;">
                                    include notice of the resolution; and</p>
                            </li>
                            <li data-list-text="(ii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                    include or be accompanied by a statement containing any information or
                                    explanation that is reasonably necessary to indicate the purpose of the
                                    resolution;</p>
                            </li>
                        </ol>
                    </li>
                    <li data-list-text="(f)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -23pt;text-align: justify;">if a
                            special resolution is intended to be moved at the meeting, specify the intention and
                            include the text of the special resolution; and</p>
                    </li>
                    <li data-list-text="(g)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -23pt;text-align: justify;">
                            contain a statement specifying a member’s right to appoint a proxy under section 596(1)
                            and (3) of the Ordinance.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Paragraph
                    (4)(e) does not apply in relation to a resolution of which—</p>
                <ol id="l97">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            notice has been included in the notice of the meeting under section 567(3) or 568(2) of
                            the Ordinance; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            notice has been given under section 615 of the Ordinance.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Despite the
                    fact that a general meeting is called by shorter notice than that specified in this article, it
                    is regarded as having been duly called if it is so agreed—</p>
                <ol id="l98">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">for
                            an annual general meeting, by all the members entitled to attend and vote at the
                            meeting; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">in
                            any other case, by a majority in number of the members entitled to attend and vote at
                            the meeting, being a majority together representing at least 95% of the total voting
                            rights at the meeting of all the members.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="36.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Persons
            entitled to receive notice of general meetings</p>
        <ol id="l99">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Notice of a
                    general meeting must be given to—</p>
                <ol id="l100">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">every
                            member; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">every
                            director.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">In paragraph
                    (1), the reference to a member includes a transmittee, if the company has been notified of the
                    transmittee’s entitlement to a share.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If notice of
                    a general meeting or any other document relating to the meeting is required to be given to a
                    member, the company must give a copy of it to its auditor (if more than one auditor, to everyone
                    of them) at the same time as the notice or the other document is given to the member.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="37.">
        <p class="s7" style="padding-top: 11pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Accidental omission to give notice of general meetings</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: justify;">Any accidental omission
            to give notice of a general meeting to, or any non-receipt of notice of a general meeting by, any person
            entitled to receive notice does not invalidate the proceedings at the meeting.</p>
    </li>
    <li data-list-text="38.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Attendance and speaking at general meetings</p>
        <ol id="l101">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A person is
                    able to exercise the right to speak at a general meeting when the person is in a position to
                    communicate to all those attending the meeting, during the meeting, any information or opinions
                    that the person has on the business of the meeting.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A person is
                    able to exercise the right to vote at a general meeting when—</p>
                <ol id="l102">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            person is able to vote, during the meeting, on resolutions put to the vote at the
                            meeting; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            person’s vote can be taken into account in determining whether or not those resolutions
                            are passed at the same time as the votes of all the other persons attending the meeting.
                        </p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may make whatever arrangements they consider appropriate to enable those attending a general
                    meeting to exercise their rights to speak or vote at it.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">In
                    determining attendance at a general meeting, it is immaterial whether any 2 or more members
                    attending it are in the same physical venue as each other.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Two or more
                    persons who are not in the same physical venue as each other attend a general meeting if their
                    circumstances are such that if they have rights to speak and vote at the meeting, they are able
                    to exercise them.</p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A person
                    attends a general meeting by using virtual meeting technology if—</p>
                <ol id="l103">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            person uses the virtual meeting technology specified in the notice of the meeting; and
                        </p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">where
                            the person has the rights to speak and vote at the meeting, the person is able to
                            exercise them.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="39.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Quorum for
            general meetings</p>
        <ol id="l104">
            <li data-list-text="(1)">
                <p style="padding-top: 1pt;padding-left: 38pt;text-indent: 7pt;line-height: 17pt;text-align: left;">
                    Two members present in person or by proxy constitute a quorum at a general meeting. (1A) For the
                    purposes of paragraph (1), a person who attends a general meeting by using the</p>
                <p style="padding-left: 71pt;text-indent: 0pt;line-height: 12pt;text-align: left;">virtual meeting
                    technology specified in the notice of the meeting is to be regarded as</p>
                <p style="padding-left: 71pt;text-indent: 0pt;text-align: left;">being present while so attending.
                </p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">No business
                    other than the appointment of the chairperson of the meeting is to be transacted at a general
                    meeting if the persons attending it do not constitute a quorum.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="40.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Chairing
            general meetings</p>
        <ol id="l105">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    chairperson (if any) of the board of directors is present at a general meeting and is willing to
                    preside as chairperson at the meeting, the meeting is to be presided over by him or her.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    present at a general meeting must elect one of themselves to be the chairperson if—</p>
                <ol id="l106">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">there is
                            no chairperson of the board of directors;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the
                            chairperson is not present within 15 minutes after the time appointed for holding the
                            meeting;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">the
                            chairperson is unwilling to act; or</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the
                            chairperson has given notice to the company of the intention not to attend the meeting.
                        </p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The members
                    present at a general meeting must elect one of themselves to be the chairperson if—</p>
                <ol id="l107">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">no
                            director is willing to act as chairperson; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">no
                            director is present within 15 minutes after the time appointed for holding the meeting.
                        </p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A proxy may be
                    elected to be the chairperson of a general meeting by a resolution of the company passed at the
                    meeting.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="41.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Attendance
            and speaking by non-members</p>
        <ol id="l108">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Directors may
                    attend and speak at general meetings, whether or not they are members of the company.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The chairperson
                    of a general meeting may permit other persons to attend and speak at a general meeting even
                    though they are not—</p>
                <ol id="l109">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">members
                            of the company; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">
                            otherwise entitled to exercise the rights of members in relation to general meetings.
                        </p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="42.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Adjournment
        </p>
        <ol id="l110">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If a quorum is
                    not present within 30 minutes after the time appointed for holding a general meeting, the
                    meeting must—</p>
                <ol id="l111">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">if
                            called on the request of members—be dissolved; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">if not
                            called on the request of members—be adjourned.</p>
                        <p style="padding-top: 4pt;padding-left: 38pt;text-indent: 0pt;text-align: justify;">(1A) If
                            a general meeting is adjourned under paragraph (1)(b), the directors must determine—</p>
                        <ol id="l112">
                            <li data-list-text="(a)">
                                <p
                                    style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                                    the date and time of the adjourned meeting;</p>
                            </li>
                            <li data-list-text="(b)">
                                <p
                                    style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                                    either or both of the following—</p>
                                <ol id="l113">
                                    <li data-list-text="(i)">
                                        <p
                                            style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: justify;">
                                            the physical venue of the adjourned meeting;</p>
                                    </li>
                                    <li data-list-text="(ii)">
                                        <p
                                            style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                            the virtual meeting technology to be used for holding the adjourned
                                            meeting; and</p>
                                    </li>
                                </ol>
                            </li>
                            <li data-list-text="(c)">
                                <p
                                    style="padding-top: 4pt;padding-left: 96pt;text-indent: -28pt;text-align: justify;">
                                    if 2 or more physical venues are determined under subparagraph (b)(i)—the
                                    principal venue, and the other venue or venues, of the adjourned meeting.</p>
                            </li>
                        </ol>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-left: 71pt;text-indent: -25pt;text-align: justify;">If at the adjourned meeting, a
                    quorum is not present within 30 minutes after the time appointed for holding the meeting, the
                    member or members present in person or by proxy constitute a quorum.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The
                    chairperson may adjourn a general meeting at which a quorum is present if—</p>
                <ol id="l114">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            meeting consents to an adjournment; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">it
                            appears to the chairperson that an adjournment is necessary to protect the safety of any
                            person attending the meeting or ensure that the business of the meeting is conducted in
                            an orderly manner.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The
                    chairperson must adjourn a general meeting if directed to do so by the meeting.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">When
                    adjourning a general meeting under paragraph (3) or (4), the chairperson must specify—</p>
                <ol id="l115">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            date and time of the adjourned meeting;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            either or both of the following—</p>
                        <ol id="l116">
                            <li data-list-text="(i)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -21pt;text-align: justify;">
                                    the physical venue of the adjourned meeting;</p>
                            </li>
                            <li data-list-text="(ii)">
                                <p
                                    style="padding-top: 4pt;padding-left: 119pt;text-indent: -24pt;text-align: justify;">
                                    the virtual meeting technology to be used for holding the adjourned meeting; and
                                </p>
                            </li>
                        </ol>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 96pt;text-indent: -28pt;text-align: justify;">if 2
                            or more physical venues are determined under subparagraph (b)(i)—the principal venue,
                            and the other venue or venues, of the adjourned meeting.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(6)">
                <p style="padding-left: 71pt;text-indent: -25pt;text-align: justify;">Only the business left
                    unfinished at the general meeting may be transacted at the adjourned meeting.</p>
            </li>
            <li data-list-text="(7)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a general
                    meeting is adjourned for 30 days or more, notice of the adjourned meeting must be given as for
                    an original meeting.</p>
            </li>
            <li data-list-text="(8)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a general
                    meeting is adjourned for less than 30 days, it is not necessary to give any notice of the
                    adjourned meeting.</p>
            </li>
            <li data-list-text="(9)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If no notice
                    of an adjourned meeting is given, a person who attends the adjourned meeting by using either of
                    the following virtual meeting technologies is to be regarded as being present while so
                    attending—</p>
                <ol id="l117">
                    <li data-list-text="(a)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            virtual meeting technology determined by the directors under paragraph (1A);</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            virtual meeting technology specified by the chairperson under paragraph (5).</p>
                        <p style="text-indent: 0pt;text-align: left;"><br /></p>
                        <h4 style="padding-left: 6pt;text-indent: 0pt;text-align: center;">Division 2—Voting at
                            General Meetings</h4>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="43.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">General
            rules on voting</p>
        <ol id="l118">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A resolution
                    put to the vote of a general meeting must be decided on a show of hands unless a poll is duly
                    demanded in accordance with these articles.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If there is
                    an equality of votes, whether on a show of hands or on a poll, the chairperson of the meeting at
                    which the show of hands takes place or at which the poll is demanded, is entitled to a second or
                    casting vote.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">On a vote on
                    a resolution on a show of hands at a general meeting, a declaration by the chairperson that the
                    resolution—</p>
                <ol id="l119">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">has
                            or has not been passed; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">has
                            passed by a particular majority,</p>
                        <p style="padding-top: 3pt;padding-left: 71pt;text-indent: 0pt;text-align: left;">is
                            conclusive evidence of that fact without proof of the number or proportion of the votes
                            recorded in favour of or against the resolution.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">An entry in
                    respect of the declaration in the minutes of the meeting is also conclusive evidence of that
                    fact without the proof.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="44.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Errors
            and disputes</p>
        <ol id="l120">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Any objection
                    to the qualification of any person voting at a general meeting may only be raised at the meeting
                    or adjourned meeting at which the vote objected to is tendered, and a vote not disallowed at the
                    meeting is valid.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Any objection
                    must be referred to the chairperson of the meeting whose decision is final.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="45.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Demanding a
            poll</p>
        <ol id="l121">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A poll on a
                    resolution may be demanded—</p>
                <ol id="l122">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">in
                            advance of the general meeting where it is to be put to the vote; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">at a
                            general meeting, either before or on the declaration of the result of a show of hands on
                            that resolution.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A poll on a
                    resolution may be demanded by—</p>
                <ol id="l123">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the
                            chairperson of the meeting;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">at least
                            2 members present in person or by proxy; or</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">any
                            member or members present in person or by proxy and representing at least 5% of the
                            total voting rights of all the members having the right to vote at the meeting.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The instrument
                    appointing a proxy is regarded as conferring authority to demand or join in demanding a poll on
                    a resolution.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A demand for a
                    poll on a resolution may be withdrawn.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="46.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Number of
            votes a member has</p>
        <ol id="l124">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">On a vote on a
                    resolution on a show of hands at a general meeting—</p>
                <ol id="l125">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">every
                            member present in person has 1 vote; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">every
                            proxy present who has been duly appointed by a member entitled to vote on the resolution
                            has 1 vote.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If a member
                    appoints more than one proxy, the proxies so appointed are not entitled to vote on the
                    resolution on a show of hands.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">On a vote on a
                    resolution on a poll taken at a general meeting—</p>
                <ol id="l126">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">every
                            member present in person has 1 vote for each share held by him or her; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">every
                            proxy present who has been duly appointed by a member has 1 vote for each share in
                            respect of which the proxy is appointed.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">This article has
                    effect subject to any rights or restrictions attached to any shares or class of shares.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="47.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Votes of
            joint holders of shares</p>
        <ol id="l127">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">For joint
                    holders of shares, only the vote of the most senior holder who votes (and any proxies duly
                    authorized by the holder) may be counted.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">For the purposes
                    of this article, the seniority of a holder of a share is determined by the order in which the
                    names of the joint holders appear in the register of members.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="48.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Votes of
            mentally incapacitated members</p>
        <ol id="l128">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A member who
                    is a mentally incapacitated person may vote, whether on a show of hands or on a poll, by the
