        <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division
            6—Distributions</h4>
    </li>
    <li data-list-text="73.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Procedure
            for declaring dividends</p>
        <ol id="l166">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The company may
                    at a general meeting declare dividends, but a dividend must not exceed the amount recommended by
                    the directors.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The directors
                    may from time to time pay the members interim dividends that appear to the directors to be
                    justified by the profits of the company.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A dividend may
                    only be paid out of the profits in accordance with Part 6 of the Ordinance.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Unless the
                    members’ resolution to declare or directors’ decision to pay a dividend, or the terms on which
                    shares are issued, specify otherwise, it must be paid by reference to each member’s holding of
                    shares on the date of the resolution or decision to declare or pay it.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Before
                    recommending any dividend, the directors may set aside out of the profits of the company any
                    sums they think fit as reserves.</p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may—</p>
                <ol id="l167">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">apply
                            the reserves for any purpose to which the profits of the company may be properly
                            applied; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            pending such an application, employ the reserves in the business of the company or
                            invest them in any investments (other than shares of the company) that they think fit.
                        </p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(7)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may also without placing the sums to reserve carry forward any profits that they think prudent
                    not to divide.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="74.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Payment
            of dividends and other distributions</p>
        <ol id="l168">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a dividend
                    or other sum that is a distribution is payable in respect of a share, it must be paid by one or
                    more of the following means—</p>
                <ol id="l169">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            transfer to a bank account specified by the distribution recipient either in writing or
                            as the directors decide;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            sending a cheque made payable to the distribution recipient by post to the distribution
                            recipient at the distribution recipient’s registered address (if the distribution
                            recipient is a holder of the share), or (in any other case) to an address specified by
                            the distribution recipient either in writing or as the directors decide;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -24pt;text-align: justify;">
                            sending a cheque made payable to the specified person by post to the specified person at
                            the address the distribution recipient has specified either in writing or as the
                            directors decide;</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            other means of payment as the directors agree with the distribution recipient either in
                            writing or as the directors decide.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">In this
                    article—</p>
                <p
                    style="padding-top: 5pt;padding-left: 71pt;text-indent: -24pt;line-height: 88%;text-align: justify;">
                    <i><b>specified person </b></i>(<span class="s6">指明人士</span>) means a person specified by the
                    distribution recipient either in writing or as the directors decide.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="75.">
        <p class="s7" style="padding-top: 3pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">No interest
            on distributions</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">The company may not pay
            interest on any dividend or other sum payable in respect of a share unless otherwise provided by—</p>
        <ol id="l170">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the terms on
                    which the share was issued; or</p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the provisions
                    of another agreement between the holder of the share and the company.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="76.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Unclaimed
            distributions</p>
        <ol id="l171">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If dividends
                    or other sums are payable in respect of shares and they are not claimed after having been
                    declared or become payable, they may be invested or made use of by the directors for the benefit
                    of the company until claimed.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The payment
                    of the dividends or other sums into a separate account does not make the company a trustee in
                    respect of it.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A
                    distribution recipient is no longer entitled to a dividend or other sum and it ceases to remain
                    owing by the company, if—</p>
                <ol id="l172">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">12
                            years have passed from the date on which the dividend or other sum became due for
                            payment; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            distribution recipient has not claimed it.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="77.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Non-cash
            distributions</p>
        <ol id="l173">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    the terms of issue of the share in question, the company may, by ordinary resolution on the
                    recommendation of the directors, decide to pay all or part of a dividend or other distribution
                    payable in respect of a share by transferring non-cash assets of equivalent value (including,
                    without limitation, shares or other securities in any company).</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">For paying a
                    non-cash distribution, the directors may make whatever arrangements they think fit, including,
                    if any difficulty arises regarding the distribution—</p>
                <ol id="l174">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">fixing
                            the value of any assets;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">paying
                            cash to any distribution recipient on the basis of that value in order to adjust the
                            rights of recipients; and</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">vesting
                            any assets in trustees.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
    <li data-list-text="78.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Waiver of
            distributions</p>
        <ol id="l175">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Distribution
                    recipients may waive their entitlement to a dividend or other distribution payable in respect of
                    a share by executing to the company a deed to that effect.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">But if the
                    share has more than one holder or more than one person is entitled to the share (whether by
                    reason of the death or bankruptcy of one or more joint holders, or otherwise), the deed is not
                    effective unless it is expressed to be executed by all the holders or other persons entitled to
                    the share.</p>
                <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division
                    7—Capitalization of Profits</h4>
            </li>
        </ol>
    </li>
    <li data-list-text="79.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Capitalization of profits</p>
        <ol id="l176">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The company
                    may by ordinary resolution on the recommendation of the directors capitalize profits.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    capitalization is to be accompanied by the issue of shares or debentures, the directors may
                    apply the sum capitalized in the proportions in which the members would be entitled if the sum
                    was distributed by way of dividend.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">To the extent
                    necessary to adjust the rights of the members among themselves if shares or debentures become
                    issuable in fractions, the directors may make any arrangements they think fit, including the
                    issuing of fractional certificates or the making of cash payments or adopting a rounding policy.
                </p>
                <h3
                    style="padding-top: 3pt;padding-left: 174pt;text-indent: 66pt;line-height: 174%;text-align: left;">
                    Part 6 Miscellaneous Provisions</h3>
                <h4 style="padding-left: 107pt;text-indent: 0pt;text-align: left;">Division 1—Communications to and
                    by Company</h4>
            </li>
        </ol>
    </li>
    <li data-list-text="80.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Means of
            communication to be used</p>
        <ol id="l177">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    these articles, anything sent or supplied by or to the company under these articles may be sent
                    or supplied in any way in which Part 18 of the Ordinance provides for documents or information
                    to be sent or supplied by or to the company for the purposes of the Ordinance.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    these articles, any notice or document to be sent or supplied to a director in connection with
                    the taking of decisions by directors may also be sent or supplied by the means by which that
                    director has asked to be sent or supplied with such a notice or document for the time being.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A director
                    may agree with the company that notices or documents sent to that director in a particular way
                    are to be deemed to have been received within a specified time of their being sent, and for the
                    specified time to be less than 48 hours.</p>
                <h4 style="padding-top: 11pt;padding-left: 6pt;text-indent: 0pt;text-align: center;">Division
                    2—Administrative Arrangements</h4>
            </li>
        </ol>
    </li>
    <li data-list-text="81.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Company
            seals</p>
        <ol id="l178">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A common seal
                    may only be used by the authority of the directors.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A common seal
                    must be a metallic seal having the company’s name engraved on it in legible form.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Subject to
                    paragraph (2), the directors may decide by what means and in what form a common seal or official
                    seal (whether for use outside Hong Kong or for sealing securities) is to be used.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">Unless
                    otherwise decided by the directors, if the company has a common seal and it is affixed to a
                    document, the document must also be signed by at least 1 director of the company and 1
                    authorized person.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">For the
                    purposes of this article, an authorized person is—</p>
                <ol id="l179">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            director of the company;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            company secretary; or</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: justify;">any
                            person authorized by the directors for signing documents to which the common seal is
                            applied.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    company has an official seal for use outside Hong Kong, it may only be affixed to a document if
                    its use on the document, or documents of a class to which it belongs, has been authorized by a
                    decision of the directors.</p>
            </li>
            <li data-list-text="(7)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    company has an official seal for sealing securities, it may only be affixed to securities by the
                    company secretary or a person authorized to apply it to securities by the company secretary.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="82.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">No right to
            inspect accounts and other records</p>
        <p style="padding-top: 4pt;padding-left: 47pt;text-indent: 0pt;text-align: left;">A person is not entitled
            to inspect any of the company’s accounting or other records or documents merely because of being a
            member, unless the person is authorized to do so by—</p>
        <ol id="l180">
            <li data-list-text="(a)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">an enactment;
                </p>
            </li>
            <li data-list-text="(b)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">an order under
                    section 740 of the Ordinance;</p>
            </li>
            <li data-list-text="(c)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">the directors;
                    or</p>
            </li>
            <li data-list-text="(d)">
                <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">an ordinary
                    resolution of the company.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="83.">
        <p class="s7" style="padding-top: 3pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Auditor’s
            insurance</p>
        <ol id="l181">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may decide to purchase and maintain insurance, at the expense of the company, for an auditor of
                    the company, or an auditor of an associated company of the company, against—</p>
                <ol id="l182">
                    <li data-list-text="(a)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            liability to any person attaching to the auditor in connection with any negligence,
                            default, breach of duty or breach of trust (except for fraud) occurring in the course of
                            performance of the duties of auditor in relation to the company or associated company
                            (as the case may be); or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">any
                            liability incurred by the auditor in defending any proceedings (whether civil or
                            criminal) taken against the auditor for any negligence, default, breach of duty or
                            breach of trust (including fraud) occurring in the course of performance of the duties
                            of auditor in relation to the company or associated company (as the case may be).</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">In this
                    article, a reference to performance of the duties of auditor includes the performance of the
                    duties specified in section 415(6)(a) and (b) of the Ordinance.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="84.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">Winding
            up</p>
        <ol id="l183">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    company is wound up and a surplus remains after the payment of debts proved in the winding up,
                    the liquidator—</p>
                <ol id="l184">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">may,
                            with the required sanction, divide amongst the members in specie or kind the whole or
                            any part of the assets of the company (whether they consist of property of the same kind
                            or not) and may, for this purpose, set a value the liquidator thinks fair on any
                            property to be so divided; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 3pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">may
                            determine how the division is to be carried out between the members or different classes
                            of members.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The
                    liquidator may, with the required sanction, vest the whole or part of those assets in trustees
                    on trust for the benefit of the contributories that the liquidator, with the required sanction,
                    thinks fit, but a member must not be compelled to accept any shares or other securities on which
                    there is any liability.</p>
            </li>
