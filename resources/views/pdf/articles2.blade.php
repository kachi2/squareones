                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">states
                            the name and address of the member appointing the proxy;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">
                            identifies the person appointed to be that member’s proxy and the general meeting in
                            relation to which that person is appointed;</p>
                    </li>
                    <li data-list-text="(c)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -24pt;text-align: left;">is
                            authenticated, or is signed on behalf of the member appointing the proxy; and</p>
                    </li>
                    <li data-list-text="(d)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">is
                            delivered to the company in accordance with these articles and any instructions
                            contained in the notice of the general meeting in relation to which the proxy is
                            appointed.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The company
