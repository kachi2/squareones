                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    chairperson of the meeting, acting in good faith, wrongly decides that an amendment to a
                    resolution is out of order, the vote on that resolution remains valid unless the Court orders
