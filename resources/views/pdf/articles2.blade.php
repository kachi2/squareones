                <ol id="l158">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            transferor or transferee may request a statement of the reasons for the refusal; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">the
                            instrument of transfer must be returned to the transferor or transferee who lodged it
                            unless the directors suspect that the proposed transfer may be fraudulent.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The
                    instrument of transfer must be returned in accordance with paragraph (2)(b) together with a
                    notice of refusal within 2 months after the date on which the instrument of transfer was lodged
                    with the company.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a request
                    is made under paragraph (2)(a), the directors must, within 28 days after receiving the request—
                </p>
                <ol id="l159">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">send
                            the transferor or transferee who made the request a statement of the reasons for the
                            refusal; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            register the transfer.</p>
                    </li>
                </ol>
            </li>
        </ol>
    </li>
