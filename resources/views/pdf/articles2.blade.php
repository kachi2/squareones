                    months after receiving the notice, the directors must—</p>
                <ol id="l163">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            register the transmittee as the holder of the share; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">send
                            the transmittee a notice of refusal of registration.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    directors refuse registration, the transmittee may request a statement of the reasons for the
                    refusal.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a request
                    is made under paragraph (3), the directors must, within 28 days after receiving the request—</p>
                <ol id="l164">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">send
                            the transmittee a statement of the reasons for the refusal; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: justify;">
                            register the transmittee as the holder of the share.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the
                    transmittee chooses to have the share transferred to another person, the transmittee must
                    execute an instrument of transfer in respect of it.</p>
            </li>
            <li data-list-text="(6)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">All the
                    limitations, restrictions and other provisions of these articles relating to the right to
                    transfer and the registration of transfer of shares apply to the notice under paragraph (1) or
                    the transfer under paragraph (5), as if the transmission had not occurred and the transfer were
                    a transfer made by the holder of the share before the transmission.</p>
            </li>
        </ol>
    </li>
