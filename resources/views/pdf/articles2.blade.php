        <p class="s7" style="padding-top: 3pt;padding-left: 47pt;text-indent: -35pt;text-align: justify;">
            Transmittees’ rights</p>
        <ol id="l161">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If a
