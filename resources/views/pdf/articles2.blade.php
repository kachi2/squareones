                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">to any
                            person or committee;</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">by any
