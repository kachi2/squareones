                    2—Decision-taking by Directors</h4>
            </li>
        </ol>
    </li>
    <li data-list-text="7.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Directors to
            take decision collectively</p>
        <ol id="l36">
            <li data-list-text="(1)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A decision of
                    the directors may only be taken—</p>
                <ol id="l37">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">by a
                            majority of the directors at a meeting; or</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">in
                            accordance with article 8.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">Paragraph (1)
                    does not apply if—</p>
                <ol id="l38">
                    <li data-list-text="(a)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">the
                            company only has 1 director; and</p>
                    </li>
                    <li data-list-text="(b)">
                        <p style="padding-top: 4pt;padding-left: 95pt;text-indent: -25pt;text-align: left;">no
                            provision of these articles requires it to have more than one director.</p>
                    </li>
                </ol>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">If paragraph (1)
