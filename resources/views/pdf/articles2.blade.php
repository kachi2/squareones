                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">The powers given
                    by this article are not limited by any other power given to the directors by these articles.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: left;">A directors’
                    meeting at which a quorum is present may exercise all powers exercisable by the directors.</p>
            </li>
        </ol>
    </li>
    <li data-list-text="4.">
        <p class="s7" style="padding-top: 12pt;padding-left: 47pt;text-indent: -35pt;text-align: left;">Members’
