                    the share transferred to another person.</p>
            </li>
            <li data-list-text="(2)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    have the same right to refuse or suspend the registration as they would have had if the holder
                    had transferred the share before the transmission.</p>
            </li>
            <li data-list-text="(3)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">A transmittee
                    is entitled to the same dividends and other advantages to which the transmittee would be
                    entitled if the transmittee were the holder of the share, except that the transmittee is not,
                    before being registered as a member in respect of the share, entitled in respect of it to
                    exercise any right conferred by membership in relation to meetings of the company.</p>
            </li>
            <li data-list-text="(4)">
                <p style="padding-top: 3pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">The directors
                    may at any time give notice requiring a transmittee to choose to become the holder of the share
                    or to have the share transferred to another person.</p>
            </li>
            <li data-list-text="(5)">
                <p style="padding-top: 4pt;padding-left: 71pt;text-indent: -25pt;text-align: justify;">If the notice
                    is not complied with within 90 days of the notice being given, the directors may withhold
                    payment of all dividends, bonuses or other moneys payable in respect of the share until the
                    requirements of the notice have been complied with.</p>
            </li>
        </ol>
    </li>
